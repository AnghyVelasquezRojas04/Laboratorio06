    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String textoBusqueda = txtBuscar.getText().toLowerCase();

        if (ListaEstudiantes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay cursos");
            return;
        }

        DefaultListModel<String> modeloLista 
                = (DefaultListModel<String>) lstEstudiantes.getModel();

        OptionalInt index = IntStream.range(0,
                modeloLista.size())
                .filter(i -> modeloLista.getElementAt(i)
                .toLowerCase().contains(textoBusqueda))
                .findFirst();

        if (index.isPresent()) {
            lstEstudiantes.setSelectedIndex(index.getAsInt());
        } else {
            JOptionPane.showMessageDialog(
                    this, 
                    "El estudiante no se encuentra en la lista", 
                    "BÃºsqueda Estudiante", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

