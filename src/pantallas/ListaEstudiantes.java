    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (ListaEstudiantes.isEmpty()){
            JOptionPane.showMessageDialog(
                    this, 
                    "No hay estudiantes");
            return;
        }
        
        int index = lstEstudiantes.getSelectedIndex();
        if(index == -1){
            JOptionPane.showMessageDialog(
                    this, 
                    "Seleccione un estudiante");
            return;
        }
        
        estudiantes.remove(index);
        ListaEstudiantes.remove(index);
    }//GEN-LAST:event_btnEliminarActionPerformed

    
    public void agregarEstudiante(Estudiante estudiante){
        estudiantes.add(estudiante);
        ListaEstudiantes.addElement(estudiante.getTextoE()+ estudiante.getTexto());
        lstEstudiantes.setModel(ListaEstudiantes);
    }
