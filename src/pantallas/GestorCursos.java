    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    
        String textoBusqueda = txtBuscar.getText().toLowerCase();

        if (listaCursos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay cursos");
            return;
        }

        DefaultListModel<String> modeloLista 
                = (DefaultListModel<String>) lstCursos.getModel();

        OptionalInt index = IntStream.range(0, modeloLista.size())
                .filter(i -> modeloLista.getElementAt(i).
                        toLowerCase().contains(textoBusqueda))
                .findFirst();

        if (index.isPresent()) {
            lstCursos.setSelectedIndex(index.getAsInt());
        } else {
            JOptionPane.showMessageDialog(
                    this, 
                    "El curso no esta en la lista", 
                    "BÃºsqueda Curso", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
            
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    
    public void agregarCurso(Curso curso){
        
        cursos.add(curso);
        listaCursos.addElement(curso.getTexto());
        lstCursos.setModel(listaCursos);
    }
    
    
